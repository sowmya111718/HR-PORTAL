// Function to get a random quote
function getRandomQuote() {
    global $motivational_quotes;
    $random_index = array_rand($motivational_quotes);
    return $motivational_quotes[$random_index];
}

// Function to get multiple random quotes (for rotating displays)
function getRandomQuotes($count = 3) {
    global $motivational_quotes;
    $random_keys = array_rand($motivational_quotes, min($count, count($motivational_quotes)));
    $quotes = [];
    
    if (is_array($random_keys)) {
        foreach ($random_keys as $key) {
            $quotes[] = $motivational_quotes[$key];
        }
    } else {
        $quotes[] = $motivational_quotes[$random_keys];
    }
    
    return $quotes;
}

// Function to get a quote by category (can be expanded later)
function getQuoteByCategory($category = 'all') {
    global $motivational_quotes;
    // This can be expanded to have categorized quotes
    return getRandomQuote();
}

// Function to get today's quote (consistent for the whole day)
function getTodaysQuote() {
    global $motivational_quotes;
    $day_of_year = date('z');
    $index = $day_of_year % count($motivational_quotes);
    return $motivational_quotes[$index];
}
